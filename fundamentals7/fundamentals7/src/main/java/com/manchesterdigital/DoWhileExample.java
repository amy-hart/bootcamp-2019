package com.manchesterdigital;

public class DoWhileExample {
    public static void main(String[] args) {
        /**
         * do {
         *     // Run some code
         * } while (boolean expression);
         */
        boolean isLying = false;

        do {
            System.out.println("Can't wait for today to finish");
        } while (isLying);
    }
}
